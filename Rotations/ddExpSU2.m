function H = ddExpSU2(q, mt)
%  =========================================================================================
%  function by Claudio Perez                                                            2023
%  -----------------------------------------------------------------------------------------
  qt  = q(1:3);
  q0  = q(4);
  H = 2*qt'*mt/q0^3*(qt*qt') + 2/q0*(qt*mt'-mt*qt'+qt'*mt*eye(3)) + 2*spin(mt);
end
